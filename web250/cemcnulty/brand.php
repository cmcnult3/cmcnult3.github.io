<h3>Concept</h3>
<p>AdventurePal is an application built to help plan and track your recreational activities. Initial functionality of the application will include:</p>
<ul>
    <li>Creating custom lists of locations (i.e., cities, public lands, rivers, beaches, trails, etc..) the user intends to visit</li>
    <li>Spaces for open ended comments about the purpose of the visit (i.e., specific experiences he/she want to have)</li>
    <li>A journaling function to update during or after a visit is complete</li>
    <li>A visit summary page to add visit dates, ratings, comments, images, etc. </li>
    <li>A querying function to review locations based on visited/ want to visit status</li>
</ul>
